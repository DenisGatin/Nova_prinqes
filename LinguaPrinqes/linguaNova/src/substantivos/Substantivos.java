package substantivos;

public class Substantivos {

	private String sub_port;
	private String sub_prin;
	
	public Substantivos(String sub_port, String sub_prin) {
		this.sub_port = sub_port;
		this.sub_prin = sub_prin;
	}

	public String getSub_port() {
		return sub_port;
	}

	public void setSub_port(String sub_port) {
		this.sub_port = sub_port;
	}

	public String getSub_prin() {
		return sub_prin;
	}

	public void setSub_prin(String sub_prin) {
		this.sub_prin = sub_prin;
	}
	
	
	
	
	
	
	
}
